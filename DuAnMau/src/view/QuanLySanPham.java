    public QuanLySanPham() {
        initComponents();
        jTable1.setModel(dtm);
        dtm.setColumnIdentifiers(new Object[]{"Id","Mã","Tên"});
        list = impl.getAllSP();
        fillTable(list);
    }
    
    private void fillTable(List<SanPham> listSP){
        dtm.setRowCount(0);
        for (SanPham sanPham : listSP) {
            dtm.addRow(sanPham.toDataRow());
        }
    }
    
    private SanPham getDataSP() {
        SanPham sp = new SanPham("", txtMa.getText(), txtTen.getText());
        return sp;
    }
    
    private void setData(int index){
        SanPham sp = list.get(index);
        txtID.setText(sp.getId());
        txtMa.setText(sp.getMa());
        txtTen.setText(sp.getTen());
    }
    
    private void clear(){
        txtID.setText("");
        txtMa.setText("");
        txtTen.setText("");
    }

